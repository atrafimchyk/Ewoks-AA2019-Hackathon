package common;

import androidx.room.Entity;
import androidx.room.PrimaryKey;

@Entity
public class VehicleService {

    @PrimaryKey
    private long date;
    private int vehicle_id;
    private int service_type_id;
    private long date_of_next_service;
    private int mileage;
    private String description;
    private double service_cost;

    public VehicleService(
            long date,
            int vehicle_id,
            int service_type_id,
            long date_of_next_service,
            int mileage,
            String description,
            double service_cost){
        setDate(date);
        setVehicle_id(vehicle_id);
        setService_type_id(service_type_id);
        setDate_of_next_service(date_of_next_service);
        setMileage(mileage);
        setDescription(description);
        setService_cost(service_cost);

    }

    public long getDate() {
        return date;
    }

    public void setDate(long date) {
        this.date = date;
    }

    public int getVehicle_id() {
        return vehicle_id;
    }

    public void setVehicle_id(int vehicle_id) {
        this.vehicle_id = vehicle_id;
    }

    public int getService_type_id() {
        return service_type_id;
    }

    public void setService_type_id(int service_type_id) {
        this.service_type_id = service_type_id;
    }

    public long getDate_of_next_service() {
        return date_of_next_service;
    }

    public void setDate_of_next_service(long date_of_next_service) {
        this.date_of_next_service = date_of_next_service;
    }

    public int getMileage() {
        return mileage;
    }

    public void setMileage(int mileage) {
        this.mileage = mileage;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public double getService_cost() {
        return service_cost;
    }

    public void setService_cost(double service_cost) {
        this.service_cost = service_cost;
    }
}
